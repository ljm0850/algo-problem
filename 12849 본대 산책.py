graph = [[1,2],[0,2,3],[0,1,3,4],[1,2,4,5],[2,3,5,7],[3,4,6],[5,7],[4,6]]
D = int(input())
dp = [[0]*(8) for _ in range(D+1)]
dp[0][0] = 1

for i in range(1,D+1):
    for j in range(8):
        for t in graph[j]:
            dp[i][j] = (dp[i][j]+ dp[i-1][t])%1000000007
print(dp[D][0])